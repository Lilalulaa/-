"""
============================================================

main.py

Точка входа в проект.

Последовательность работы:

1. Поиск Excel-файлов.
2. Парсинг файлов.
3. Создание базы SQLite.
4. Загрузка данных.
5. Вывод статистики.

============================================================
"""

from pathlib import Path
import logging

from parser import DebtExcelParser
from database import Database


# ----------------------------------------------------------
# Настройка логирования
# ----------------------------------------------------------

logging.basicConfig(
    level=logging.INFO,
    format="%(asctime)s | %(levelname)s | %(message)s",
    datefmt="%H:%M:%S"
)

logger = logging.getLogger(__name__)


# ----------------------------------------------------------
# Пути проекта
# ----------------------------------------------------------

PROJECT_DIR = Path(__file__).parent

# Папка с Excel
DATA_DIR = PROJECT_DIR / "data"

# База данных
DATABASE_FILE = PROJECT_DIR / "dashboard.db"


def main() -> None:
    """
    Основная функция проекта.
    """

    logger.info("=" * 70)
    logger.info("Запуск обработки отчетов")
    logger.info("=" * 70)

    # ------------------------------------------------------
    # Создаем парсер
    # ------------------------------------------------------

    parser = DebtExcelParser()

    # ------------------------------------------------------
    # Парсим все Excel-файлы
    # ------------------------------------------------------

    documents = parser.parse_folder(DATA_DIR)

    logger.info("Получено документов: %s", len(documents))

    # ------------------------------------------------------
    # Создаем БД
    # ------------------------------------------------------

    db = Database(DATABASE_FILE)

    # Сначала подключаемся
    db.connect()

    # Затем создаем таблицы
    db.create_tables()

    # Затем очищаем
    db.clear()

    # ------------------------------------------------------
    # Загружаем документы
    # ------------------------------------------------------

    db.insert_many(documents)

    # ------------------------------------------------------
    # Выводим статистику
    # ------------------------------------------------------

    logger.info("-" * 70)

    logger.info(
        "Документов в БД: %s",
        db.count_documents()
    )

    logger.info(
        "Менеджеров: %s",
        db.count_managers()
    )

    logger.info(
        "Контрагентов: %s",
        db.count_contractors()
    )

    logger.info(
        "Общий долг: %.2f",
        db.get_total_debt()
    )

    logger.info(
        "Сумма реализации: %.2f",
        db.get_total_amount()
    )

    logger.info("-" * 70)

    db.close()

    logger.info("Работа завершена.")


if __name__ == "__main__":

    main()